#include<iostream>
using namespace std;
int main()
{
    cout<<"Lift shift :-"<<endl;
    cout<<"(5<<1) = "<<(5<<1)<<"\n";
    cout<<"(5<<2) = "<<(5<<2)<<"\n";
    cout<<"(-5<<2) = "<<(-5<<2);


    cout<<"\n\nRight shift :-"<<endl;
    cout<<"(5>>1) = "<<(5>>1)<<"\n";
    cout<<"(5>>2) = "<<(5>>2)<<"\n";
    cout<<"(-5>>2) = "<<(-5>>2);
    return 0;
}
